package homeworkweek6;

public class EighteenthProgramme {
    public static void main(String[] args) {
        int p= 125;
        int q = 24;
        int r = p + q;
        System.out.println("125+24="+r);//addition=149
        int s =p-q;
        System.out.println("125-24="+s);//division=101
        int t=p*q;
        System.out.println("125X24="+t);//multiplication=3001
        int u=p/q;
        System.out.println("125/24="+u);//division=5
        int v=p%q;
        System.out.println("125 mod 24="+v);//125 mod 24=5


    }
}



